class Mouse{

int price,weight,buttons,sensitivity,modelNo,year,stock,code,warranty,length,width,height;

Mouse(){
System.out.println("Default Constructor Called");
}

Mouse(int price,int weight,int buttons,int sensitivity,int modelNo,int year,int stock,int code,int warranty,int length,int width,int height){

this.price=price;
this.weight=weight;
this.buttons=buttons;
this.sensitivity=sensitivity;
this.modelNo=modelNo;
this.year=year;
this.stock=stock;
this.code=code;
this.warranty=warranty;
this.length=length;
this.width=width;
this.height=height;
}
}
